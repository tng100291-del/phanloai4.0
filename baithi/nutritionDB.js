function generateFreshDB() {
    let FINAL_DB = {};
    
    FRESH_FOOD_SOURCE.forEach(item => {
        let name = item[0];
        let group = item[1];
        let overrides = item[2] || {};
        let profile = PROFILES[group];

        let newData = {};
        NUTRITION_KEYS.forEach(k => {
            // Đặt giá trị RẤT THẤP cho Vit D (p34) và B12 (p33) trừ trường hợp đặc biệt (Tảo Xoắn)
            if (k.id === "p33" && item[0] !== "Tảo Xoắn (Tươi)" || k.id === "p34") {
                newData[k.id] = 0.00; 
                return;
            }
            
            let val = overrides[k.id] !== undefined ? overrides[k.id] : (profile[k.id] || 0);
            newData[k.id] = parseFloat(val.toFixed(2));
        });

        FINAL_DB[name] = newData;
    });

    return FINAL_DB;
}

